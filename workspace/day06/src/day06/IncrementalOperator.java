package day06;

public class IncrementalOperator {
	public static void main(String[]args) {
		//증감연산자
		
		int a = 10;
		//후위형:우선순위가 매우 느림
		System.out.println(a++);
		
		
		
		int b = a++;
		
		
		
		
		//전위형:우선순위가 매우 빠름
		System.out.println(++a);
		
		System.out.println(b);
		


	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}
}